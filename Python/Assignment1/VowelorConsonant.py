#Vowel or Consonant

Alphabet = input("Enter Alphabet to check Vowel or Consonant: ")
if (Alphabet == 'a' or Alphabet == 'e' or Alphabet == 'i' or Alphabet == 'o' or Alphabet == 'u'):
    print(f"Given Alphabet '{Alphabet}' is an Vowel")
else:
    print(f"Given Alphabet '{Alphabet}' is a Consonant")